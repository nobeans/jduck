package org.coderepos.lang.java.jduck.exception;

public class InterfaceNotFoundRuntimeException extends RuntimeException {

    public InterfaceNotFoundRuntimeException() {
        super();
    }

    public InterfaceNotFoundRuntimeException(String message, Throwable cause) {
        super(message, cause);
    }

    public InterfaceNotFoundRuntimeException(String message) {
        super(message);
    }

    public InterfaceNotFoundRuntimeException(Throwable cause) {
        super(cause);
    }

}
